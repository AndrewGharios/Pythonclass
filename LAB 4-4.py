Tupe = (5,-21,-2,4,9,-8,3,2,-1)  #creating the original Tupe
print("Original Tupe: ", str(Tupe))

userlist = []
for x in Tupe:
    if x > 0:       #equation that will append only positive numbers from Tupe into a list
        userlist.append(x)
    userlist.sort()

print("New Tuple with Positive number: ", str(tuple(userlist)))


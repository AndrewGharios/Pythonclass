if user_charac >= "A" and user_charac <= "Z":
    print("Uppercase character was entered")
elif user_charac >= "a" and user_charac <= "z":
    print("Lowercase character was entered")
elif user_charac >= "0" and user_charac <= "9":
    print("A number was entered")
else: print("Error")


